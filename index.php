            $(function () {
                $(".ck").on('click', function(){
                    var nisn    = $("#nisn").val();
                    if (nisn == "") {
                        alert("Terjadi Kesalahan! NISN belum diisi.");
                        return;
                    }
                    $("#hk").modal('toggle');

                    /* ============================= Update DATA APTH==================================*/
                    $.ajax({
                    type: "POST",
                    url: "tampil_hasil.php",
                    data: {nisn:nisn},
                    //dataType: "json",
                        success: function(data)
                        {
                            //alert(data);
                            $("#show_lulus").html(data).refresh;
                        }
                    });

                });
            });
